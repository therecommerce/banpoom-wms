package com.sgsone.returnrescue.vo.rest;

public class RestRequest {
	
	public RestHeader header;
	
	public RestBody body;

	public RestHeader getHeader() {
		return header;
	}

	public void setHeader(RestHeader header) {
		this.header = header;
	}

	public RestBody getBody() {
		return body;
	}

	public void setBody(RestBody body) {
		this.body = body;
	}

	
}
